<script setup lang="ts">
import { computed } from "vue"
import { Guitar } from "../interfaces/interfaces"

interface Props {
  cart: Guitar[]
  guitar: Guitar
}

const { cart, guitar } = defineProps<Props>()

const emits = defineEmits<{
  incrementQuantity: [id: number]
  decrementQuantity: [id: number]
  addToCart: [guitar: Guitar]
  deleteProduct: [id: number]
  emptyCart: []
}>()

const totalPay = computed(() => {
  return cart.reduce(
    (total, guitar) => total + guitar.price * guitar.quantity,
    0
  )
})
</script>

<template>
  <header class="py-5 header">
    <div class="container-xl">
      <div class="row justify-content-center justify-content-md-between">
        <div class="col-8 col-md-3">
          <a href="index.html">
            <img class="img-fluid" src="/img/logo.svg" alt="imagen logo" />
          </a>
        </div>
        <nav
          class="col-md-6 a mt-5 d-flex align-items-start justify-content-end"
        >
          <div class="carrito">
            <img
              class="img-fluid"
              src="/img/carrito.png"
              alt="imagen carrito"
            />

            <div id="carrito" class="bg-white p-3">
              <p v-if="cart.length === 0" class="text-center m-0">
                Cart is Empty
              </p>

              <template v-else>
                <table class="w-100 table">
                  <thead>
                    <tr>
                      <th>Image</th>
                      <th>Name</th>
                      <th>Price</th>
                      <th>Quantity</th>
                      <th></th>
                    </tr>
                  </thead>
                  <tbody>
                    <tr v-for="guitar in cart" :key="guitar.id">
                      <td>
                        <img
                          class="img-fluid"
                          :src="`/img/${guitar.image}.jpg`"
                          :alt="guitar.name"
                        />
                      </td>
                      <td>{{ guitar.name }}</td>
                      <td class="fw-bold">${{ guitar.price }}</td>
                      <td class="flex align-items-start gap-4">
                        <button
                          type="button"
                          class="btn btn-dark"
                          @click="emits('decrementQuantity', guitar.id)"
                        >
                          -
                        </button>
                        {{ guitar.quantity }}
                        <button
                          type="button"
                          class="btn btn-dark"
                          @click="emits('incrementQuantity', guitar.id)"
                        >
                          +
                        </button>
                      </td>
                      <td>
                        <button
                          class="btn btn-danger"
                          type="button"
                          @click="emits('deleteProduct', guitar.id)"
                        >
                          X
                        </button>
                      </td>
                    </tr>
                  </tbody>
                </table>

                <p class="text-end">
                  Total: <span class="fw-bold">${{ totalPay }}</span>
                </p>
                <button
                  class="btn btn-dark w-100 mt-3 p-2"
                  @click="emits('emptyCart')"
                >
                  Empty cart
                </button>
              </template>
            </div>
          </div>
        </nav>
      </div>
      <!--.row-->

      <div class="row mt-5">
        <div class="col-md-6 text-center text-md-start pt-5">
          <h1 class="display-2 fw-bold">{{ guitar.name }} Model</h1>
          <p class="mt-5 fs-5 text-white">
            {{ guitar.description }}
          </p>
          <p class="text-primary fs-1 fw-black">${{ guitar.price }}</p>
          <button
            type="button"
            class="btn fs-4 bg-primary text-white py-2 px-5"
            @click="emits('addToCart', guitar)"
          >
            Add to Cart
          </button>
        </div>
      </div>
    </div>

    <img
      class="header-guitarra"
      src="/img/header_guitarra.png"
      alt="imagen header"
    />
  </header>
</template>

